package com.nitesh.leetsolution;

public class paperpdf {
    public String name;
    public String url;

    public paperpdf() {
    }

    public paperpdf(String name, String url) {
        this.name = name;
        this.url = url;
    }

    public String getName() {
        return name;
    }

    public String getUrl() {
        return url;
    }


}